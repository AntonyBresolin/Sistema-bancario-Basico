    private void jButtonSaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaqueActionPerformed
        // TODO add your handling code here:
        ContaDAO cDao = new ContaDAO();

        String salvarRetorno;
        String quantidade = JOptionPane.showInputDialog(null, "Digite o valor de dinheiro que deseja Sacar");

        for (Conta c : cDao.readForLogin(System.getProperty("loginUsuarioDoBD"))) {
            salvarRetorno = c.sacar(Double.parseDouble(quantidade));

            if (salvarRetorno.equals("Sucesso")) {

                JOptionPane.showMessageDialog(this, "Depósito realizado com sucesso!");

            } else {
                JOptionPane.showMessageDialog(this, "Deposito de valor inferior a R$ 0");
                return;
            }
            break;
        }
    }//GEN-LAST:event_jButtonSaqueActionPerformed

