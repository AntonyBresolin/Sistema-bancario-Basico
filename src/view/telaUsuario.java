    private void jButtonDepositoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDepositoActionPerformed
        // TODO add your handling code here:
        ContaDAO cDao = new ContaDAO();

        String quantidade = JOptionPane.showInputDialog(null, "Digite a quantidade de dinheiro deseja depositar");

        for (Conta c : cDao.readForLogin(System.getProperty("loginUsuarioDoBD"))) {

            if (c.depositar(Double.parseDouble(quantidade)) == true) {
//                cDao.update(c);
                JOptionPane.showMessageDialog(this, "Deposito realizado com sucesso!");
                break;
            } else {

                JOptionPane.showMessageDialog(this, "Erro ao realizar o deposito");
            }
        }


    }//GEN-LAST:event_jButtonDepositoActionPerformed

